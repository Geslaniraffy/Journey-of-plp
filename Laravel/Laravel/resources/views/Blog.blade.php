<p>Hello everyone. For our first post about our journey, let me introduce my self. I'm Ryan Bron from CS Batch 2022. I am a computer science student at Pamantasan ng Lungsod ng Pasig. We are so grateful to be the part of this university.
If you are a computer science student, of course you know a lot about coding. If you do not know what is coding, you can read or search the article about coding. for my first time to try coding, it’s hard for me because I never learn it before. But yeah, do not be sad because you must believe that learning by doing is very effective for us. You can learn from sololearn, w3school, and many more.</p>
<p>So my journey started with in our first semester, During the first week of the class, I was overwhelmed by how much work we were required to do. You need to finish the homework, which is probably the only required homework for most of other classes, redefine the keywords learned in the week. Needless to say, the first week of the class was hectic for me. However, once I settled in the pace of this class, I understood the importance of all these exercises.
For my second semester, I am trying to join some curricular activities. Like some competitions relating to mobile games and other activities. The reason why I joined some activities because someone said that “To be a computer science student, you must explore a lot of things. If you just learn about the materials by your lecturer, it never enough tho”. So yeah, I tried the some of it. For my first competition, my teams just to be the finalist of that competition. But don’t be sad. The important thing is you must try again and again until your dream’s come true. And finally, my dream’s come true!</p>
<p>In my third semester, I would like to say how much I learned in this course. Professor Ronnie Chu as one of the most professional and understanding instructors I have had at the Pamantasan ng Lungsod ng Pasig. He was very responsive to student's questions and was very fair in the grading process. Although his class was difficult, I believe that he helped students grasp very complex subjects. The work assigned was time-consuming and demanding, but did prepare the student thoroughly for the mid-term and final exams. We took the class from Prof. Chu and I was told that he was one of the best at communicating the concepts. I was not disappointed.
For our fourth semester, I was worried whether I should take an online course or not, specially Programming subjects, because I really didn't know if it was going to be as informative and as effective as a classical course. Our online learning experience as a student is not so good. I only like how we don’t have to wake up at 6 every day. What I don’t like about it is that for me it’s confusing when a teacher gives us work mostly since it’s easier for us to do work with instructions that the teacher gives. Being in a physical classroom makes us only focus on our work, but online learning makes it harder to focus because we have to also focus on household stuff, and it throws us off. We would not want to do online learning in the future because it is hard for us to concentrate on our work only. I really learned a lot from our Prof. Tan. He is one of the best in his field. He really helps a lot if you have a problem with an assignment or in trouble trying to finish it on time. The assignments first may seem a little bit long, but as time passes you get used to the subject and finish them quicker. Also if you are stuck with some of the questions, there is always someone from the class to ask in the discussions section. I really enjoyed this course and I recommend it to everyone.</p>
<p>My online learning experience has been different than what I’m used to. I love that I get to study from the comfort of my home; however, I would rather be in a physical classroom doing my work because it gives me the opportunity to ask questions when needed, and in my own opinion, it is much more effective than online learning. Compared to learning in a physical classroom online classes seem more difficult because you don’t get the hands-on learning like you do in a physical classroom. In the future, I’d choose a physical classroom over online any day. The reason being, it’s easier for me to understand the lesson plans and put my new knowledge into effect. 
Students are grateful for technology, supportive social networks, the natural environment and appreciated the flexibility to renegotiate their time. Technology is enabling students to maintain connections and support classes to continue. Giving and receiving assistance to others despite physical distance has created a new sense of social inclusion and many students were newly grateful for outdoor space and nature.</p>
<p>For my fifth semester, I was to start off by saying that the online course content was perfect - well prepared and easy to follow. The computational tools that are available online is an effective learning tool that helped me understand the concepts better. Some of our professor are very effective teacher. Without their interest and enthusiasm I would not have increased my knowledge of the subject to the level I am at now. The choice of homework problems was simple, but good enough to understand the concepts. Prof. Chu is a great instructor. He was very responsive to any question I had and always make sure that everything was clear. He is a "true" teacher as he was at the same time encouraging, positive, and visibly cared about our progress. In addition, he was also eager to learn from his students and always welcomed feedback. I truly enjoyed taking this class. The first thing I would recommend to anybody taking this class is to keep up with the homework. You can easily submit each week's assignments in parts.
For our sixth semester ? From all of the things that you’ve read before, we just want to share some experiences with you. Hopefully, we can motivate you to be more active to join any events or competitions. “Practice makes perfect. Don’t worry to try new challenges if you don’t try it, you never know how difficult it is”. We think sharing session is very important to get insight from anyone who more senior. Don’t forget to make a list of your dream.</p>
